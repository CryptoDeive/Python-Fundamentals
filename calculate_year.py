#Crea una lista que contenga los gastos de cada mes. Por ejemplo:
gastos = [100, 150, 200, 250, 300, 350, 400, 450, 500, 550, 600, 650]

#Utiliza la funci√≥n sum para calcular la suma total de los gastos.
total_gastos = sum(gastos)

#Divide la suma total de los gastos por la cantidad de meses para obtener el promedio.
